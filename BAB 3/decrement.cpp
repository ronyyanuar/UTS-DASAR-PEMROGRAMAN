#include <iostream>
using namespace std;

int main()
{
    int k;
    float l;

    k = 100;
    l = 10.5;

    cout << "Nilai k awal : " << k << endl;
    cout << "Nilai --k : " << --k << endl;
    cout << "Nilai l akhir : " << k << endl;

    cout << "Nilai l awal : " << l << endl;
    cout << "Nilai --l : " << l-- << endl;
    cout << "Nilai l akhir : " << l << endl;

    return 0;
}
